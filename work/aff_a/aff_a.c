#include <unistd.h>

int		main(int count, char **value)
{
	int	i;

	i = 0;
	if (count != 2)
	{
		write(1, "a", 1);
	}
	else
	{
		while (value[1][i] != '\0')
		{
			if (value[1][i] == 'a')
			{
				write(1, &value[1][i], 1);	
				break ;
			}
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
